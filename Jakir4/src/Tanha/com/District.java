
package Tanha.com;

import java.util.logging.Logger;

public class District {
    private int code;
    private String districtName;

    public District(int code, String districtName) {
        this.code = code;
        this.districtName = districtName;
    }

    public int getCode() {
        return code;
    }

    public String getDistrictName() {
        return districtName;
    }
   
    

    
}
