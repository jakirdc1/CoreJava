
public class Cow {
    private String name;
    private int age;
    private double price;

    public Cow(String name, int age, double price) {
        this.name = name;
        this.age = age;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public double getPrice() {
        return price;
    }
    
    
}
